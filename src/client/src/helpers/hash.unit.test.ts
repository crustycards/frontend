import {SHA256} from './hash';

describe('SHA256', () => {
  it('hashes data correctly', () => {
    expect(SHA256('hello')).toEqual('2CF24DBA5FB0A30E26E83B2AC5B9E29E1B161E5C1FA7425E73043362938B9824');
    expect(SHA256('world')).toEqual('486EA46224D1BB4FB680F34F7C9AD96A8F24EC88BE73EA8E5A6C65260E9CB8A7');
    expect(SHA256('HeLlO')).toEqual('1BC5E82F506DD1F2883CC3D2B6EC34E25D1CB36C6081E1DE0A86F2FD9D2D8CA3');
    expect(SHA256('WoRlD')).toEqual('86ECC8D5B1536705E65CF0032D1C560F0924247A35D33F363A5E256AACD08531');
    expect(SHA256(' ')).toEqual('36A9E7F1C95B82FFB99743E0C5C4CE95D83C9A430AAC59F84EF3CBFAB6145068');
  });
});
